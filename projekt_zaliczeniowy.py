def visualize_results(model, test_loader):
    model.eval()
    with torch.no_grad():
        for inputs, labels in test_loader:
            inputs, labels = inputs.to(device), labels.to(device)
            outputs, feature_maps = model(inputs)
            _, preds = torch.max(outputs, 1)

            for i in range(inputs.size(0)):
                if preds[i] != labels[i]:
                    img_np = inputs[i].cpu().numpy().transpose((1, 2, 0))
                    f_map = feature_maps[i].cpu().numpy()
                    f_map_avg = np.mean(f_map, axis=0)

                    fig, axes = plt.subplots(1, 2, figsize=(10, 5))
                    axes[0].imshow(np.clip(img_np * 0.229 + 0.485, 0, 1))
                    axes[0].set_title(f"Prawda: {class_names[labels[i]]}\nPredykcja: {class_names[preds[i]]}")
                    axes[0].axis('off')

                    axes[1].imshow(f_map_avg, cmap='viridis')
                    axes[1].set_title("Mapa cech (Warstwa Conv1)")
                    axes[1].axis('off')

                    plt.show()
                    return

